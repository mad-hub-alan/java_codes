package Rough;

public class PrintAtoZ{
    public static void main(String[] args){
        for(char x='A'; x<='Z'; x++){
            System.out.println(x);
        }

        for(double i=0; i<10; i++){
            System.out.print('*'+" ");
        }

        for(boolean x=true; x!=false; ){
            System.out.println("Hii");
        }
    }
}